public class CurlyBracketPractice {
    public static void main(String[] args) {
        if (5==5)
            System.out.println("Hi");
        System.out.println("Hello");  //if its true its going to run everything Hi and Hello


        System.out.println("=============");


        if (5==8)
            System.out.println("Hi");  // if its False its going to affect only first statement //shows only Hallo
        System.out.println("Hello");






    }
}
